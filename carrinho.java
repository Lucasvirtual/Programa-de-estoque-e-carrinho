    private void btnValorFinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValorFinalActionPerformed
        String pagamento = cmbPagamento.getSelectedItem().toString();
        String total= txtTotal.getText();
        String valorFinal = txtValorFinal.getText();
        
        double totalN = Double.parseDouble(total);
        double valorFinalN = Double.parseDouble(valorFinal);
        
        if(pagamento.equals("Crédito")){
            valorFinalN = (totalN * 1.1);
            txtValorFinal.setText(""+valorFinalN);
        }else if(pagamento.equals("Débito")){
            valorFinalN = (totalN * 1.05);
            txtValorFinal.setText(""+valorFinalN);
        }else{
            valorFinalN = (totalN * 0.85);
            txtValorFinal.setText(""+valorFinalN);
        }
    }//GEN-LAST:event_btnValorFinalActionPerformed

