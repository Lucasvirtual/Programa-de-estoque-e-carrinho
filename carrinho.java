    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String codigo = txtCodigo.getText();
        String produto = txtProduto.getText();
        String quantidade = txtQuantidade.getText();
        String valor = txtValor.getText();
        String total = txtTotal.getText();
        
        
        
        double quantidadeN = Double.parseDouble(quantidade);
        double valorN = Double.parseDouble(valor);
        double totalN = Double.parseDouble(total);
        
        totalN += (quantidadeN * valorN);
        
        
        
        
        DefaultTableModel tabelaProduto = (DefaultTableModel) tblProdutos.getModel();
        
        Object novoProduto[] = new Object[]{
            codigo,
            produto,
            quantidade,
            valor
        };
        
        
        
        tabelaProduto.addRow(novoProduto);
        
        txtCodigo.setText("");
        txtProduto.setText("");
        txtQuantidade.setText("");
        txtValor.setText("");
        txtTotal.setText("" + totalN);
    }//GEN-LAST:event_btnAdicionarActionPerformed

